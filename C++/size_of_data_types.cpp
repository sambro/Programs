#include<iostream>

using namespace std;

int main()
{
    std::cout<<"The size of char : "<<sizeof(char)<<"byte"<<endl;
    std::cout<<"The size of int : "<<sizeof(int)<<"bytes"<<endl;
    std::cout<<"The size of long int : "<<sizeof(long int)<<"bytes"<<endl;
    std::cout<<"The size of signed long int : "<<sizeof(signed long int)<<"bytes"<<endl;
    std::cout<<"The size of unsigned long int : "<<sizeof(unsigned long int)<<"bytes"<<endl;
    std::cout<<"The size of wchar_t : "<<sizeof(wchar_t)<<"bytes"<<endl;

    return 0;
}
