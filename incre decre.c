#include<stdio.h>
int main()
{
    int x=2;

    printf("%d\n",x++);   ///post incre 2
    printf("%d\n",x);     ///no incre 3

    printf("%d\n",++x);   ///pre incre 4

    printf("%d\n",x);     ///no incre 4








     ///DECREMENT


     printf("%d",x--); ///4
     printf("%d",x);///3

     printf("%d",--x);///2

     printf("%d",x);///2

    return 0;
}
